// ============================================================
// DATABASE INITIALIZATION SCRIPT — Schema Creation
// ============================================================
// Creates all required PostgreSQL tables, indexes, and constraints.
// Run this script once to set up the database schema.
// Safe to run multiple times — uses CREATE TABLE IF NOT EXISTS.
//
// Usage: npm run db:init
// ============================================================

// Load environment variables FIRST — database connection needs these
require("dotenv").config();

// Import the database pool for executing DDL statements
const { pool } = require("../config/database");

// Import logger for initialization progress tracking
const logger = require("../utils/logger");

// ============================================================
// SQL statements for creating the complete database schema
// Each table is created with IF NOT EXISTS for idempotency
// ============================================================

const CREATE_TABLES_SQL = `
-- ============================================================
-- Enable UUID generation extension (PostgreSQL 13+ has gen_random_uuid built-in)
-- gen_random_uuid() creates cryptographically secure UUID v4 values
-- This is the foundation of our immutable identity system
-- ============================================================
CREATE EXTENSION IF NOT EXISTS "pgcrypto";

-- ============================================================
-- USERS TABLE — Core Identity Store
-- internal_user_id (UUID) is the ETERNAL identity anchor
-- email_hash stores the bcrypt hash for privacy-preserving lookups
-- normalized_email is stored in plaintext for human reference only
-- user_role tracks the latest role assignment
-- is_active enables soft-delete without losing identity history
-- ============================================================
CREATE TABLE IF NOT EXISTS users (
  -- Primary identity: UUID v4, generated by PostgreSQL, globally unique
  -- This UUID NEVER changes, even if the user's email changes
  -- NEVER exposed to the client — internal server-side use only
  internal_user_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  
  -- bcrypt hash of the normalized email — used for privacy-preserving storage
  -- UNIQUE constraint prevents duplicate user registrations
  -- bcrypt includes random salt, making rainbow table attacks impossible
  email_hash VARCHAR(255) NOT NULL,
  
  -- Human-readable email stored for admin reference and display
  -- This is NOT the identity — the UUID is the identity
  -- UNIQUE constraint ensures one account per email address
  normalized_email VARCHAR(255) UNIQUE NOT NULL,
  
  -- Current role assignment — changes over time via admin or pattern matching
  -- Roles: 'pending', 'student', 'faculty', 'admin', etc.
  -- Default 'pending' means authenticated but not yet role-assigned
  -- NOTE: 'current_role' is a PostgreSQL reserved keyword, so we use 'user_role'
  user_role VARCHAR(50) DEFAULT 'pending',
  
  -- Soft-delete flag — false means the account is deactivated
  -- Deactivated accounts cannot authenticate but remain in the database
  -- Essential for compliance: identity history must be preserved
  is_active BOOLEAN DEFAULT true,
  
  -- Timestamp of account creation — set once, never changed
  -- Used for account age calculations and audit reporting
  created_at TIMESTAMPTZ DEFAULT NOW(),
  
  -- Timestamp of last modification — updated on role/status changes
  -- Helps track when the last administrative action occurred
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Index on email_hash for fast bcrypt comparison lookups during login
-- B-tree index on the hash column for O(log n) access
CREATE INDEX IF NOT EXISTS idx_users_email_hash ON users(email_hash);

-- Index on normalized_email for human-readable lookups by admins
-- Also used in the login pipeline for user existence checks
CREATE INDEX IF NOT EXISTS idx_users_normalized_email ON users(normalized_email);

-- ============================================================
-- USER IDENTITY SNAPSHOTS TABLE — Immutable Audit Trail
-- APPEND-ONLY: Rows are NEVER updated or deleted
-- Records the EXACT identity state at each login moment
-- Essential for compliance, security investigations, and debugging
-- ============================================================
CREATE TABLE IF NOT EXISTS user_identity_snapshots (
  -- Unique snapshot identifier — each login gets its own UUID
  snapshot_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  
  -- Reference to the user who logged in — foreign key to users table
  -- ON DELETE CASCADE removes snapshots if the user is hard-deleted
  -- (but we use soft-delete, so this rarely triggers)
  internal_user_id UUID REFERENCES users(internal_user_id) ON DELETE CASCADE,
  
  -- The email address used at the EXACT moment of login
  -- May differ from the user's current email if they changed it in Google
  -- This is the HISTORICAL truth — what email was used when
  email_at_login VARCHAR(255) NOT NULL,
  
  -- The role assigned at the EXACT moment of login
  -- May differ from user_role if the role was changed later
  -- This is the HISTORICAL truth — what role they had when logging in
  role_at_login VARCHAR(50) NOT NULL,
  
  -- Server timestamp of the login event — immutable
  -- Set by PostgreSQL's NOW(), not the client, for accuracy
  login_timestamp TIMESTAMPTZ DEFAULT NOW(),
  
  -- Google's unique subject ID from the ID token
  -- Stable across email changes — Google's permanent user identifier
  -- Useful for cross-referencing with Google Workspace admin tools
  google_id_sub VARCHAR(255)
);

-- Composite index for querying a user's login history ordered by time
-- Supports the common query: "Show me all logins for user X, newest first"
CREATE INDEX IF NOT EXISTS idx_snapshots_user_login 
  ON user_identity_snapshots(internal_user_id, login_timestamp DESC);

-- Index on login_timestamp for time-based queries across all users
-- Supports: "Show all logins in the last 24 hours"
CREATE INDEX IF NOT EXISTS idx_snapshots_login_time 
  ON user_identity_snapshots(login_timestamp);

-- ============================================================
-- USER SESSIONS TABLE — JWT Audit Log & Revocation Store
-- Each JWT issued by the system has a corresponding session record
-- Provides: audit trail, concurrent session tracking, revocation
-- ============================================================
CREATE TABLE IF NOT EXISTS user_sessions (
  -- Unique session identifier
  session_id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  
  -- Reference to the user this session belongs to
  internal_user_id UUID REFERENCES users(internal_user_id) ON DELETE CASCADE,
  
  -- The JWT's jti (JWT Token ID) claim — unique per-token identifier
  -- Used to look up and revoke specific tokens
  -- UNIQUE constraint prevents duplicate session records
  jwt_token_id VARCHAR(100) UNIQUE NOT NULL,
  
  -- Server timestamp when the JWT was issued
  issued_at TIMESTAMPTZ DEFAULT NOW(),
  
  -- Calculated expiration time of the JWT
  -- Used for cleanup queries to remove expired sessions
  expires_at TIMESTAMPTZ NOT NULL,
  
  -- Client's User-Agent header at login time — device fingerprinting
  -- Useful for detecting suspicious login patterns across devices
  user_agent TEXT,
  
  -- Client's IP address at login time
  -- PostgreSQL's INET type supports both IPv4 and IPv6
  -- Used for geographic analysis of login patterns
  ip_address INET,
  
  -- Whether this session has been revoked (logout or security action)
  -- If true, the JWT is rejected even if cryptographically valid
  revoked BOOLEAN DEFAULT false,
  
  -- Timestamp of when the session was revoked (null if still active)
  -- Helps measure time-to-revocation for security metrics
  revoked_at TIMESTAMPTZ
);

-- Index on jwt_token_id for fast token lookup during every authenticated request
-- The auth middleware checks this on each request for revocation status
CREATE INDEX IF NOT EXISTS idx_sessions_token_id ON user_sessions(jwt_token_id);

-- Composite index for querying a user's sessions ordered by time
-- Supports: "Show all sessions for user X, newest first"
CREATE INDEX IF NOT EXISTS idx_sessions_user_sessions 
  ON user_sessions(internal_user_id, issued_at DESC);

-- ============================================================
-- ALLOWED DOMAINS TABLE — Domain Whitelist Configuration
-- Controls which email domains are permitted to authenticate
-- Core zero-trust boundary — only approved organizations can log in
-- ============================================================
CREATE TABLE IF NOT EXISTS allowed_domains (
  -- Auto-incrementing domain identifier
  domain_id SERIAL PRIMARY KEY,
  
  -- The domain pattern to match (e.g., 'bitsathy.ac.in')
  -- UNIQUE constraint prevents duplicate domain entries
  domain_pattern VARCHAR(255) UNIQUE NOT NULL,
  
  -- Whether this domain is currently active (enabled)
  -- Supports temporary domain disabling without deletion
  is_active BOOLEAN DEFAULT true,
  
  -- Timestamp of when this domain was added
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- ============================================================
-- ROLE PATTERNS TABLE — Email-to-Role Mapping Rules
-- Automatically assigns roles based on email patterns
-- Uses SQL LIKE syntax: '%.student@bitsathy.ac.in' matches all students
-- Priority determines which pattern wins if multiple match
-- ============================================================
CREATE TABLE IF NOT EXISTS role_patterns (
  -- Auto-incrementing pattern identifier
  pattern_id SERIAL PRIMARY KEY,
  
  -- SQL LIKE pattern for email matching
  -- % matches any sequence of characters
  -- _ matches any single character
  email_pattern VARCHAR(255) NOT NULL,
  
  -- The role to assign when the pattern matches
  assigned_role VARCHAR(50) NOT NULL,
  
  -- Priority order — lower number = higher priority
  -- If multiple patterns match, the lowest priority number wins
  priority INTEGER DEFAULT 10,
  
  -- Whether this pattern is currently active
  -- Supports temporary disabling without deletion
  is_active BOOLEAN DEFAULT true
);
`;

// ============================================================
// Main initialization function
// Connects to PostgreSQL and executes the schema creation SQL
// ============================================================
const initDatabase = async () => {
  console.log("========================================");
  console.log("DATABASE INITIALIZATION STARTING");
  console.log("========================================\n");

  try {
    // Execute all CREATE TABLE statements in a single query
    // PostgreSQL processes them sequentially within the connection
    await pool.query(CREATE_TABLES_SQL);

    console.log("✓ All tables created successfully");
    console.log("✓ All indexes created successfully");
    console.log("\n========================================");
    console.log("DATABASE INITIALIZATION COMPLETE");
    console.log("========================================");
    console.log('\nNext step: Run "npm run db:seed" to seed initial data');
  } catch (error) {
    console.error("✗ Database initialization FAILED:", error.message);
    console.error("\nFull error:", error);
    process.exit(1);
  } finally {
    // Close the pool to allow the script to exit cleanly
    await pool.end();
  }
};

// Run the initialization
initDatabase();
